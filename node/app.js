const calc = require('./calc');

// Now you can use the functions from the `calc` module
console.log(calc.add(2, 3)); // Output: 5
console.log(calc.subtract(5, 2)); // Output: 3
console.log(calc.multiply(4, 6)); // Output: 24
console.log(calc.divide(10, 2)); // Output: 5
console.log(calc.square(3)); // Output: 9
console.log(calc.sum(1, 2, 3, 4, 5)); // Output: 15
